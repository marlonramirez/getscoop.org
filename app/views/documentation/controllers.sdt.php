<p>La función de un controlador es servir de intermediario entre la vista y el modelo, cualquier petición
http es recibida por el controlador despues de haber sido ruteada hasta su respectivo método.</p>

<p>Scoop basa su iteroperatibilidad entre sistemas bajo la tecnica REST, por lo cual trae un
sistema nativo para el manejo de metodos HTTP.</p>